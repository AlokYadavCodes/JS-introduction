console.log("Converting string to Number")
let score="45a" //string
console.log(typeof(score))
let IntValueOfScore=Number(score)
console.log(typeof(IntValueOfScore))
console.log(IntValueOfScore)

console.log("Converting Null to Number")
score=null //null
console.log(typeof(score))
IntValueOfScore=Number(score)
console.log(typeof(IntValueOfScore))
console.log(IntValueOfScore)

console.log("Converting undefined to Number")
let points  //undefined
console.log(typeof(points))
let IntValueOfpoints=Number(points)
console.log(typeof(IntValueOfpoints))
console.log(IntValueOfpoints)
